export function getTerrainTypeImageUrl(type: TerrainType): string | null {
  const code = (type.symbolCode ?? '').trim();
  if (!code) {
    return null;
  }

  const fileName = ISOM_FILE_BY_CODE[code];
  return fileName ? `${OMAP_BASE}${fileName}` : null;
}
